% -include_lib("eunit/include/eunit.hrl").
% -include_lib("kernel/include/logger.hrl").

%===============================================================================
%                            APPLICATION CALLBACKS
%===============================================================================

-export([start/2, 
         stop/1]).

%===============================================================================
%                            APPLICATION CALLBACKS
%===============================================================================

%-------------------------------------------------------------------------------
% START/2
%
%-------------------------------------------------------------------------------
-spec start(application:start_type(), term()) -> {ok, pid()} | 
                                                 {ok, pid(), term()} |
                                                 {error, term()}.
%-------------------------------------------------------------------------------
start(_StartType, _Args) ->
    case tcp_echo_sup:start_link() of
        {ok, Pid} ->
            {ok, Pid};
        Other ->
            {error, Other}
    end.

%-------------------------------------------------------------------------------
% STOP/1
%
%-------------------------------------------------------------------------------
stop(_State) ->
    ok.

